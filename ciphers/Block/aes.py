def pad_key(key, length=16):
    kb = key.encode("utf-8")
    return (kb * (length // len(kb) + 1))[:length]

def pad_text(text, block=16):
    tb = text.encode("utf-8")
    pad_len = block - (len(tb) % block)
    return tb + bytes([pad_len] * pad_len)

